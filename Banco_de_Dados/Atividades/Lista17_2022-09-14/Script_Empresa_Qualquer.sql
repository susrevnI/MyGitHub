# Criar tabelas:
create table Empregados (
    codMatricula_Empregado integer not null primary key,
    cod_Departamento integer not null references Departamentos(cod_Departamento),
    nome_Empregado varchar(45),
    sobrenome_Empregado varchar(45),
    sexo_Empregado char(1),
    telefone_Empregado varchar(45),
    dataAdimissao_Empregado datetime,
    cargo_Empregado varchar(45)
);

create table Departamentos (
    cod_Departamento integer not null primary key,
    codGerente_Departamento integer not null references Empregados(codMatricula_Empregado),
    nome_Departamento varchar(45),
    sigla_Departamento char(2)
);

create table Projetos (
    cod_Projeto integer not null primary key,
    titulo_Projeto varchar(90),
    horasPrevistas_Projeto numeric(5,2)
);

create table Alocacao_Projetos (
    cod_Projeto integer not null references Projetos(cod_Projeto),
    codMatricula_Empregado integer not null references Empregados(codMatricula_Empregado),
    dataAlocacao_Projeto datetime not null
);

create table Dependentes (
    codMatricula_Empregado integer not null references Empregados(codMatricula_Empregado),
    nome_Dependente varchar(45),
    sobrenome_Dependente varchar(45),
    relacaoEmpregado_Dependente varchar(45),
    telefone_Dependente varchar(45),
    sexo_Dependente char(1)
);

