create database HospitalVeterinario;

use HospitalVeterinario;

create table Pet (
	cod_Pet integer not null,
    nome_Pet varchar(45),
    enfermidade varchar(45),
    especie varchar(45),
    primary key (cod_Pet)
);

create table Cliente (
	cod_Cliente integer not null,
    cpf_Cliente varchar(45),
    nome_Cliente varchar(45),
    sobrenome_Cliente varchar(45),
    telefone_Cliente varchar(45),
    email_Cliente varchar(45),
    endereco varchar(90),
    primary key (cod_Cliente)
);

create table Veterinario (
	cod_Veterinario integer not null,
    cpf_Veterinario varchar(45),
    nome_Veterinario varchar(45),
    sobrenome_Veterinario varchar(45),
    telefone_Veterinario varchar(45),
    email_Veterinario varchar(45),
    endereco varchar(90),
    especialidade varchar(45),
    salario numeric(10,2),
    primary key (cod_Veterinario)
);

create table Consulta (
	cod_Consulta integer not null,
	cod_Cliente integer,
    cod_Pet integer,
    cod_Veterinario integer,
    _data date,
    primary key (cod_Consulta)
);

alter table Consulta add constraint consulta_cliente_fk foreign key (cod_Cliente) references Cliente(cod_Cliente);
alter table Consulta add constraint consulta_pet_fk foreign key (cod_Pet) references Pet(cod_Pet);
alter table Consulta add constraint consulta_veterinario_fk foreign key (cod_Veterinario) references Veterinario(cod_Veterinario);

