# Criar banco de dados:
create database locadoraVeiculos;

# Usar banco de dados:
use locadoraVeiculos;

# Criar tabelas:
create table Clientes (
    cpf_Cliente varchar(45) not null primary key,
    nome_Cliente varchar(45),
    sobrenome_Cliente varchar(45),
    endereco_Cliente varchar(90),
    telefone_Cliente varchar(45),
    dataNasc_Cliente datetime,
    idade_Cliente integer,
    email_Cliente varchar(45)
);

create table Veiculos (
    placa_Veiculo varchar(45) not null primary key,
    marca_Veiculo varchar(45),
    modelo_Veiculo varchar(45),
    cor_Veiculo varchar(45),
    combustivel_Veiculo varchar(45)
);

create table Contratos (
    num_Contrato integer not null primary key,
    cpf_Cliente varchar(45) not null references Clientes(cpf_Cliente),
    placa_Veiculo varchar(45) not null unique references Veiculos(placa_Veiculo),
    data_Contrato datetime,
    precoDiaria_Contrato numeric(10,2),
    valorSeguro_Contrato numeric(10,2)
);

create table Manutencoes (
    id_Manutencao varchar(45) not null primary key,
    placa_Veiculo varchar(45) not null unique references Veiculos(placa_Veiculo),
    custoMedMensal_Manutencao numeric(10,2),
    data_Manutencao datetime
);

